class NumToP8Converter(object):

    def __init__(self):
        self.prev_char_code:int = -1

    def num_to_p8(self, char_code:int):
        char:str
        # If output char is a number string 0-9 (char codes 48-57), 
        # make sure it doesn't get mixed with previous '\\0', by returning the char as \\0{char_code}
        if char_code >= 48 and char_code <= 57 and self.prev_char_code == 0:
            char = f"\\0{char_code}"
        else:
            char = num_to_p8_table[char_code]
        self.prev_char_code = char_code
        return char
    


